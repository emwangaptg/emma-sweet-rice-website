const nav = document.querySelector("[data-nav]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const productGrid = document.querySelector("[data-product-grid]");
const categoryButtons = document.querySelectorAll("[data-category]");
const liveUpdated = document.querySelector("[data-live-updated]");
const instagramGrid = document.querySelector("[data-instagram-grid]");
const modal = document.querySelector("[data-modal]");
const modalImage = document.querySelector("[data-modal-image]");
const modalTitle = document.querySelector("[data-modal-title]");
const modalPrice = document.querySelector("[data-modal-price]");
const modalSummary = document.querySelector("[data-modal-summary]");
const modalSpecs = document.querySelector("[data-modal-specs]");
const modalIngredients = document.querySelector("[data-modal-ingredients]");

function productCategory(product) {
  const name = product.name;
  if (/組|禮盒|食盛/.test(name)) return "gift";
  if (/蛋糕|吐司|巴斯克|麵包|貝果|派/.test(name)) return "cake";
  return "cookie";
}

function escapeHtml(value) {
  const entities = { "&": "&amp;", "<": "&lt;", ">": "&gt;", "\"": "&quot;" };
  return String(value).replace(/[&<>"]/g, (char) => entities[char]);
}

function updateHeader() {
  header.classList.toggle("scrolled", window.scrollY > 20);
}

function closeMenu() {
  nav.classList.remove("open");
  header.classList.remove("open");
  menuToggle.setAttribute("aria-expanded", "false");
}

function renderProducts() {
  productGrid.innerHTML = PRODUCTS.map((product) => {
    const category = productCategory(product);
    const label = category === "gift" ? "禮盒組合" : category === "cake" ? "蛋糕麵包" : "餅乾點心";
    return [
      '<button class="product-card" type="button" data-product-id="' + escapeHtml(product.id) + '" data-product-category="' + category + '">',
      '<img src="' + escapeHtml(product.image) + '" alt="' + escapeHtml(product.name) + '" loading="lazy" />',
      '<span class="product-card-body">',
      '<span class="product-tag">' + label + '</span>',
      '<h3>' + escapeHtml(product.name) + '</h3>',
      '<strong class="product-price">' + escapeHtml(product.priceText) + '</strong>',
      '<p>' + escapeHtml(product.summary) + '</p>',
      '</span>',
      '</button>'
    ].join('');
  }).join('');
}

function formatUpdatedAt(value) {
  if (!value) return "尚未更新";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return value;
  return new Intl.DateTimeFormat("zh-TW", {
    dateStyle: "medium",
    timeStyle: "short",
    timeZone: "Asia/Taipei"
  }).format(date);
}

function renderFreshUpdates() {
  if (liveUpdated) {
    liveUpdated.textContent = formatUpdatedAt(SITE_DATA_UPDATED_AT);
  }

  if (!instagramGrid) return;
  instagramGrid.innerHTML = INSTAGRAM_POSTS.slice(0, 1).map((post) => [
    '<a class="instagram-card" href="' + escapeHtml(post.url || IG_URL) + '" target="_blank" rel="noreferrer">',
    '<img src="' + escapeHtml(post.image || "assets/youmitian-logo.png") + '" alt="' + escapeHtml(post.title || "Instagram 貼文") + '" loading="lazy" />',
    '<span>',
    '<strong>' + escapeHtml(post.title || "Instagram 貼文") + '</strong>',
    '<small>' + escapeHtml(post.caption || "到 IG 查看最新貼文") + '</small>',
    '</span>',
    '</a>'
  ].join('')).join('');
}

function openProduct(product) {
  modalImage.src = product.image;
  modalImage.alt = product.name;
  modalTitle.textContent = product.name;
  modalPrice.textContent = product.priceText;
  modalSummary.textContent = product.detail;
  modalIngredients.textContent = product.ingredients || "依賣貨便商品頁與包裝標示為準。";
  modalSpecs.innerHTML = product.specs.map((spec) => '<li>' + escapeHtml(spec) + '</li>').join('');
  modal.hidden = false;
  document.body.classList.add("modal-open");
}

function closeModal() {
  modal.hidden = true;
  document.body.classList.remove("modal-open");
}

window.addEventListener("scroll", updateHeader, { passive: true });
updateHeader();
renderProducts();
renderFreshUpdates();

menuToggle.addEventListener("click", () => {
  const willOpen = !nav.classList.contains("open");
  nav.classList.toggle("open", willOpen);
  header.classList.toggle("open", willOpen);
  menuToggle.setAttribute("aria-expanded", String(willOpen));
});

nav.addEventListener("click", (event) => {
  if (event.target.matches("a")) closeMenu();
});

categoryButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const category = button.dataset.category;
    categoryButtons.forEach((item) => item.classList.toggle("active", item === button));
    document.querySelectorAll("[data-product-category]").forEach((card) => {
      card.hidden = category !== "all" && card.dataset.productCategory !== category;
    });
  });
});

productGrid.addEventListener("click", (event) => {
  const card = event.target.closest("[data-product-id]");
  if (!card) return;
  const product = PRODUCTS.find((item) => item.id === card.dataset.productId);
  if (product) openProduct(product);
});

modal.addEventListener("click", (event) => {
  if (event.target.matches("[data-close-modal]")) closeModal();
});

window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && !modal.hidden) closeModal();
});
